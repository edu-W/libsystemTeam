-- ============================================================
-- 2. 座位表 (lib_seat)
-- ============================================================
CREATE TABLE IF NOT EXISTS lib_seat (
    seat_id        VARCHAR(20)  NOT NULL PRIMARY KEY COMMENT '座位编号，格式: 1-A-001',
    floor          VARCHAR(10)  NOT NULL COMMENT '楼层（数字），如 1 / 2 / 3 / 4',
    area           VARCHAR(10)  NOT NULL COMMENT '区域字母，如 A / B / C',
    status         VARCHAR(20)  NOT NULL DEFAULT 'available' COMMENT '状态: available/booked/used/temporary/closed',
    user_account   VARCHAR(50)  NULL     COMMENT '当前占用账号',
    book_time      DATETIME     NULL     COMMENT '预约时间（30分钟超时）',
    leave_time     DATETIME     NULL     COMMENT '暂离时间（30分钟超时）',
    check_in_time  DATETIME     NULL     COMMENT '实际入座时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- ============================================================
-- 3. 座位记录表 (seat_record) - 用于历史记录 / 违规统计
-- ============================================================
CREATE TABLE IF NOT EXISTS seat_record (
    id          INT          NOT NULL AUTO_INCREMENT PRIMARY KEY,
    account     VARCHAR(50)  NOT NULL COMMENT '账号',
    seat_id     VARCHAR(20)  NOT NULL COMMENT '座位编号',
    action_type VARCHAR(30)  NOT NULL COMMENT '操作类型: book / finish / violation',
    create_time TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '记录时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- ============================================================
-- 4. 举报表 (report)
-- ============================================================
CREATE TABLE IF NOT EXISTS report (
    id               INT          NOT NULL AUTO_INCREMENT PRIMARY KEY,
    reported_account VARCHAR(50)  NOT NULL COMMENT '被举报账号',
    report_type      VARCHAR(100) NOT NULL COMMENT '举报类型',
    seat_id          VARCHAR(20)  NULL     COMMENT '相关座位',
    report_time      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '举报时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

