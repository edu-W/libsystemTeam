-- ============================================================
-- 1. 用户表 (Users)
-- ============================================================
CREATE TABLE IF NOT EXISTS Users (
    account        VARCHAR(50)  NOT NULL PRIMARY KEY COMMENT '账号（学号或工号）',
    password       VARCHAR(100) NOT NULL COMMENT '密码',
    name           VARCHAR(50)  NOT NULL COMMENT '姓名',
    role           VARCHAR(20)  NOT NULL DEFAULT 'student' COMMENT '角色: student / admin',
    credit         INT          NOT NULL DEFAULT 100 COMMENT '信用分',
    status         VARCHAR(20)  NOT NULL DEFAULT 'normal' COMMENT '状态: normal / frozen',
    freeze_end_time DATETIME    NULL     COMMENT '冻结到期时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

